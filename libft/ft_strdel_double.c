#include "libft.h"

void		ft_strdel_double(char **s)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i])
			free(s[i]);
		i++;
	}
	if (s[i])
		free(s[i]);
	if (s)
		free(s);
}
