#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9') ||
			(c > 64 && c < 91) ||
			(c > 96 && c < 123) ||
			(c == '|') ||
			(c == '-'))
		return (1);
	return (0);
}
